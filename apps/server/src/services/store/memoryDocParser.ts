export const parseMemoryDoc = (content: string, profile: MemoryDocProfile): MemoryDocIndex => {
  const parsedFrontmatter = parseFrontmatter(content)
  const warnings: string[] = []
  let body = content
  let frontmatter: MemoryDocFrontmatter = {
    socrates_doc: profile.docType,
    schema_version: 1,
    owner_tool: profile.ownerTool,
    scope: profile.scope,
  }
  if (profile.indexTags && profile.indexTags.length > 0) {
    frontmatter = { ...frontmatter, index_tags: profile.indexTags }
  }
  let bodyLineOffset = 0
  if (parsedFrontmatter) {
    body = parsedFrontmatter.body
    bodyLineOffset = parsedFrontmatter.bodyLineOffset
    const validated = memoryDocFrontmatterSchema.safeParse(parsedFrontmatter.data)
    if (validated.success) {
      frontmatter = validated.data
      if (frontmatter.socrates_doc !== profile.docType) {
        warnings.push(`Frontmatter doc type ${frontmatter.socrates_doc} did not match expected ${profile.docType}.`)
      }
      if (frontmatter.owner_tool !== profile.ownerTool) {
        warnings.push(`Frontmatter owner ${frontmatter.owner_tool} did not match expected ${profile.ownerTool}.`)
      }
      if (frontmatter.scope !== profile.scope) {
        warnings.push(`Frontmatter scope ${frontmatter.scope} did not match expected ${profile.scope}.`)
      }
    } else {
      warnings.push("Frontmatter was present but did not match the Socrates memory-doc schema.")
    }
  } else {
    warnings.push("Document has no Socrates memory-doc frontmatter.")
  }

  const sections = parseSections(body, bodyLineOffset)
  const required = memoryDocRequiredSections[profile.docType]
  const existing = new Set(sections.map((section) => section.sectionId))
  for (const requiredSection of required) {
    if (!existing.has(requiredSection)) {
      warnings.push(`Missing required section ${requiredSection}.`)
    }
  }

  return {
    path: profile.path,
    scope: profile.scope,
    ...(profile.projectId ? { projectId: profile.projectId } : {}),
    docType: profile.docType,
    ownerTool: profile.ownerTool,
    schemaVersion: frontmatter.schema_version,
    contentHash: hashText(content),
    sections,
    ...(warnings.length > 0 ? { warnings } : {}),
  }
}

export const ensureStructuredMemoryDoc = (filePath: string, profile: MemoryDocProfile): void => {
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  if (!fs.existsSync(filePath)) {
    fs.writeFileSync(filePath, buildStructuredMemoryDoc(profile))
    return
  }
  const current = fs.readFileSync(filePath, "utf8")
  try {
    const parsed = parseMemoryDoc(current, profile)
    if (isValidStructuredMemoryDoc(parsed, profile.docType)) {
      return
    }
  } catch {
    // Preserve the original text in a legacy section instead of failing startup on a malformed doc.
  }
  fs.writeFileSync(filePath, buildStructuredMemoryDoc(profile, { legacyContent: current }))
}

export const buildStructuredMemoryDoc = (profile: MemoryDocProfile, options: { legacyContent?: string } = {}): string => {
  const title = titleForProfile(profile)
  const frontmatter = [
    "---",
    `socrates_doc: ${profile.docType}`,
    "schema_version: 1",
    `owner_tool: ${profile.ownerTool}`,
    `scope: ${profile.scope}`,
    profile.indexTags && profile.indexTags.length > 0 ? `index_tags: [${profile.indexTags.join(", ")}]` : undefined,
    "---",
    "",
  ].filter((line): line is string => typeof line === "string")
  const sections = sectionDefinitions[profile.docType].map(renderSection)
  const legacy = options.legacyContent?.trim()
  if (legacy) {
    sections.push(
      renderSection({
        id: "legacy_content",
        kind: "legacy",
        heading: "Legacy Content",
        tags: ["migration"],
        body: legacy,
      }),
    )
  }
  return [...frontmatter, `# ${title}`, "", ...sections].join("\n").replace(/\n{3,}/g, "\n\n").trimEnd() + "\n"
}

export const patchMemoryDocSection = (content: string, profile: MemoryDocProfile, sectionId: string, oldText: string, newText: string, replaceAll = false): string => {
  const parsed = parseMemoryDoc(content, profile)
  const section = parsed.sections.find((item) => item.sectionId === sectionId)
  if (!section) {
    throw new SocratesError("memory_doc_section_not_found", `Section ${sectionId} was not found in ${profile.path}.`, {
      recoverable: true,
      details: { path: profile.path, sectionId },
    })
  }
  const occurrences = oldText.length === 0 ? 0 : countOccurrences(section.content, oldText)
  if (oldText.length === 0 || occurrences === 0) {
    throw new SocratesError("memory_doc_section_patch_failed", `oldText was not found in section ${sectionId}.`, {
      recoverable: true,
      details: { path: profile.path, sectionId },
    })
  }
  if (!replaceAll && occurrences > 1) {
    throw new SocratesError("memory_doc_section_patch_ambiguous", "oldText matched more than once in the selected section. Retry with a longer oldText or replaceAll=true.", {
      recoverable: true,
      details: { path: profile.path, sectionId, occurrences },
    })
  }
  const lines = content.split(/\r?\n/)
  const sectionLines = lines.slice(section.lineStart, section.lineEnd - 1)
  const currentSection = sectionLines.join("\n")
  const nextSection = replaceAll ? currentSection.split(oldText).join(newText) : currentSection.replace(oldText, newText)
  lines.splice(section.lineStart, section.lineEnd - section.lineStart - 1, ...nextSection.split("\n"))
  const next = lines.join("\n")
  const reparsed = parseMemoryDoc(next, profile)
  if (!isValidStructuredMemoryDoc(reparsed, profile.docType)) {
    throw new SocratesError("memory_doc_invalid_after_patch", "Section patch would leave the memory doc structurally invalid.", {
      recoverable: true,
      details: { path: profile.path, sectionId, warnings: reparsed.warnings },
    })
  }
  return next.endsWith("\n") ? next : `${next}\n`
}

export const stampMemoryDocFrontmatter = (
  content: string,
  stamp: { updatedAt: string; updatedBy: MemoryDocOwnerTool | "system"; lastEditedSection?: string },
): string => {
  const normalized = content.replace(/\r\n/g, "\n")
  const entries = [
    ["updated_at", stamp.updatedAt],
    ["updated_by", stamp.updatedBy],
    ...(stamp.lastEditedSection ? ([["last_edited_section", stamp.lastEditedSection]] as const) : []),
  ] as const
  if (!normalized.startsWith("---\n")) {
    return [
      "---",
      ...entries.map(([key, value]) => `${key}: ${quoteYamlScalar(value)}`),
      "---",
      "",
      normalized,
    ].join("\n")
  }
  const end = normalized.indexOf("\n---\n", 4)
  if (end < 0) {
    return normalized
  }
  const raw = normalized.slice(4, end)
  const body = normalized.slice(end + "\n---\n".length)
  let lines = raw.split("\n")
  for (const [key, value] of entries) {
    const nextLine = `${key}: ${quoteYamlScalar(value)}`
    const index = lines.findIndex((line) => line.trim().startsWith(`${key}:`))
    if (index >= 0) {
      lines[index] = nextLine
    } else {
      lines = [...lines, nextLine]
    }
  }
  return `---\n${lines.join("\n")}\n---\n${body}`
}

