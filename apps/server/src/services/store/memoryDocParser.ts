const isValidStructuredMemoryDoc = (index: MemoryDocIndex, docType: MemoryDocType): boolean => {
  if (index.warnings?.some((warning) => warning.startsWith("Missing required section") || warning.toLowerCase().includes("frontmatter"))) {
    return false
  }
  const required = memoryDocRequiredSections[docType]
  const sectionIds = new Set(index.sections.map((section) => section.sectionId))
  return required.every((sectionId) => sectionIds.has(sectionId))
}

const renderSection = (section: SectionDefinition): string =>
  [
    `<!-- socrates:section id="${section.id}" kind="${section.kind}" tags="${(section.tags ?? []).join(",")}" -->`,
    `## ${section.heading}`,
    "",
    section.body ?? "",
    "<!-- /socrates:section -->",
    "",
  ].join("\n")

const parseSections = (body: string, lineOffset: number): MemoryDocSection[] => {
  const lines = body.split(/\r?\n/)
  const sections: MemoryDocSection[] = []
  const seen = new Set<string>()
  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index] ?? ""
    const open = /^<!--\s*socrates:section\s+(.+?)\s*-->\s*$/.exec(line)
    if (!open) {
      continue
    }
    const attributes = parseAttributes(open[1] ?? "")
    const sectionId = attributes.id
    if (!sectionId) {
      throw new SocratesError("memory_doc_section_invalid", "Socrates section marker is missing an id.", { recoverable: true, details: { line: index + 1 + lineOffset } })
    }
    if (seen.has(sectionId)) {
      throw new SocratesError("memory_doc_section_duplicate", `Duplicate Socrates section id ${sectionId}.`, { recoverable: true, details: { sectionId } })
    }
    seen.add(sectionId)
    const closeIndex = lines.findIndex((candidate, candidateIndex) => candidateIndex > index && /^<!--\s*\/socrates:section\s*-->\s*$/.test(candidate))
    if (closeIndex < 0) {
      throw new SocratesError("memory_doc_section_unclosed", `Socrates section ${sectionId} is missing a closing marker.`, { recoverable: true, details: { sectionId } })
    }
    const contentLines = lines.slice(index + 1, closeIndex)
    const content = contentLines.join("\n").trimEnd()
    sections.push({
      sectionId,
      kind: attributes.kind || "general",
      tags: (attributes.tags ?? "").split(",").map((tag) => tag.trim()).filter(Boolean),
      heading: firstHeading(content) ?? sectionId,
      content,
      lineStart: index + 1 + lineOffset,
      lineEnd: closeIndex + 1 + lineOffset,
      contentHash: hashText(content),
      summary: summarizeSection(content),
      tokenEstimate: estimateTokens(content),
    })
    index = closeIndex
  }
  return sections
}

const parseFrontmatter = (content: string): { data: Record<string, unknown>; body: string; bodyLineOffset: number } | undefined => {
  const normalized = content.replace(/\r\n/g, "\n")
  if (!normalized.startsWith("---\n")) {
    return undefined
  }
  const end = normalized.indexOf("\n---\n", 4)
  if (end < 0) {
    return undefined
  }
  const raw = normalized.slice(4, end)
  const body = normalized.slice(end + "\n---\n".length)
  const data: Record<string, unknown> = {}
  for (const line of raw.split("\n")) {
    const match = /^([A-Za-z0-9_-]+):\s*(.*)$/.exec(line.trim())
    if (!match) {
      continue
    }
    const key = match[1]!
    const value = match[2] ?? ""
    if (/^\d+$/.test(value)) {
      data[key] = Number(value)
    } else if (value.startsWith("[") && value.endsWith("]")) {
      data[key] = value.slice(1, -1).split(",").map((item) => item.trim()).filter(Boolean)
    } else {
      data[key] = value.replace(/^["']|["']$/g, "")
    }
  }
  return { data, body, bodyLineOffset: raw.split("\n").length + 2 }
}

const parseAttributes = (raw: string): Record<string, string> => {
  const attributes: Record<string, string> = {}
  const pattern = /([A-Za-z0-9_-]+)="([^"]*)"/g
  let match: RegExpExecArray | null
  while ((match = pattern.exec(raw)) !== null) {
    attributes[match[1]!] = match[2] ?? ""
  }
  return attributes
}

const quoteYamlScalar = (value: string): string => `"${value.replaceAll("\\", "\\\\").replaceAll('"', '\\"')}"`

const firstHeading = (content: string): string | undefined => {
  const line = content.split(/\r?\n/).find((item) => /^#{1,6}\s+/.test(item))
  return line?.replace(/^#{1,6}\s+/, "").trim()
}

const summarizeSection = (content: string): string => {
  const lines = content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#"))
    .slice(0, 3)
  const summary = lines.join(" ").replace(/\s+/g, " ").trim()
  return summary.length > 240 ? `${summary.slice(0, 237)}...` : summary
}

const titleForDocType = (docType: MemoryDocType): string =>
  docType
    .split("_")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ")

const titleForProfile = (profile: MemoryDocProfile): string => {
  if (profile.docType === "tool_doc") {
    const name = path.basename(profile.path, ".md").replace(/[_-]+/g, " ").trim()
    return `${name || "Tool"} Usage Guide`
  }
  return titleForDocType(profile.docType)
}

const hashText = (text: string): string => createHash("sha256").update(text).digest("hex")
const estimateTokens = (text: string): number => Math.ceil(text.length / 4)
const countOccurrences = (text: string, needle: string): number => text.split(needle).length - 1
